#include "ft_printf.h"

void	ft_putstr(char	*str)
{
	int	i;

	if (str == NULL)
	{
		return ;
	}
	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
